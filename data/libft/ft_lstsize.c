#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*lst_i;

	i = 0;
	lst_i = lst;
	while (lst_i != NULL)
	{
		i++;
		lst_i = lst_i->next;
	}
	return (i);
}
