#include "libft.h"

int	ft_power(int nb, int power)
{
	int	i;
	int	res;

	i = 0;
	res = nb;
	if (power <= 0)
		return (1);
	while (i < power - 1)
	{
		res *= nb;
		i++;
	}
	return (res);
}
